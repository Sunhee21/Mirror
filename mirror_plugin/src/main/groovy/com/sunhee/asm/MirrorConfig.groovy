package com.sunhee.asm;

/**
 *
 * 插件配置信息
 * @author sunhee
 * @intro
 * @date 2019/6/16
 */
class MirrorConfig {

    public static boolean anno

    public static HashSet<String> filter

    static boolean getAnnotation() {
        return anno
    }

    static void setAnnotation(boolean a) {
        anno = a
    }

    static HashSet<String> getFilter() {
        return filter
    }

    static void setFilter(HashSet<String> fle) {
        filter = fle
    }

}
